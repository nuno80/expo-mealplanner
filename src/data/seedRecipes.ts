import { randomUUID } from "expo-crypto";

export interface SeedRecipe {
	id: string;
	nameIt: string;
	nameEn: string;
	slug: string;
	descriptionIt: string;
	descriptionEn: string;
	category: "breakfast" | "lunch" | "dinner" | "snack";
	prepTimeMin: number;
	cookTimeMin: number;
	totalTimeMin: number;
	servings: number;
	difficulty: "easy" | "medium" | "hard";
	kcalPer100g: number;
	proteinPer100g: number;
	carbsPer100g: number;
	fatPer100g: number;
	fiberPer100g: number;
	kcalPerServing: number;
	servingWeightG: number;
	isPublished: boolean;
}

function makeRecipe(
	nameIt: string,
	nameEn: string,
	category: SeedRecipe["category"],
	kcal: number,
	protein: number,
	carbs: number,
	fat: number,
	fiber: number,
	servingG: number,
	prepMin: number,
	cookMin: number,
	difficulty: SeedRecipe["difficulty"] = "easy",
	descIt = "",
	descEn = "",
): SeedRecipe {
	const slug = nameIt
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, "-")
		.replace(/-+$/, "");
	return {
		id: randomUUID(),
		nameIt,
		nameEn,
		slug,
		descriptionIt: descIt || `Ricetta italiana: ${nameIt}`,
		descriptionEn: descEn || `Italian recipe: ${nameEn}`,
		category,
		prepTimeMin: prepMin,
		cookTimeMin: cookMin,
		totalTimeMin: prepMin + cookMin,
		servings: 1,
		difficulty,
		kcalPer100g: kcal,
		proteinPer100g: protein,
		carbsPer100g: carbs,
		fatPer100g: fat,
		fiberPer100g: fiber,
		kcalPerServing: Math.round((kcal * servingG) / 100),
		servingWeightG: servingG,
		isPublished: true,
	};
}

